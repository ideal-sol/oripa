import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import process from "node:process";

const bundleUrl = new URL(
  "../../../openapi/bundled/public.openapi.json",
  import.meta.url,
);
const outputUrl = new URL("../src/generated/public-contract.ts", import.meta.url);

const bundleBytes = await readFile(bundleUrl);
const bundle = JSON.parse(bundleBytes.toString("utf8"));
const operationIds = [];
for (const pathItem of Object.values(bundle.paths ?? {})) {
  for (const method of [
    "get",
    "head",
    "post",
    "put",
    "patch",
    "delete",
    "options",
    "trace",
  ]) {
    const operation = pathItem?.[method];
    if (operation && typeof operation.operationId === "string") {
      operationIds.push(operation.operationId);
    }
  }
}
operationIds.sort();

const generated = `/**
 * This file is generated from openapi/bundled/public.openapi.json.
 * Do not edit this file directly.
 */

export const PUBLIC_CONTRACT_FIXTURE = {
  openapi: ${JSON.stringify(bundle.openapi)},
  operation_count: ${operationIds.length},
  operation_ids: ${JSON.stringify(operationIds)},
  bundle_sha256: ${JSON.stringify(createHash("sha256").update(bundleBytes).digest("hex"))},
} as const;
`;

if (process.argv[2] === "--write") {
  await writeFile(outputUrl, generated);
} else if (process.argv[2] === "--check") {
  const current = await readFile(outputUrl, "utf8");
  if (current !== generated) {
    process.stderr.write("Public Contract Fixtureの再生成差分があります。\n");
    process.exitCode = 1;
  }
} else {
  process.stderr.write("Usage: generate-public-contract.mjs --write|--check\n");
  process.exitCode = 2;
}
